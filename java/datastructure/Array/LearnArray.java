package datastructure.Array;

public class LearnArray {
    public static void main(String[] args) {

        String[] cars = {"Volvo", "BMW", "Ford"};

        System.out.println(cars);
        System.out.println(cars[0]);

        cars[0] = "Opel";
        cars[2] = "Mazda";

        System.out.println(cars);

    }
}
